//exemplo 1
let contador = 1
while (contador <= 10) {
    console.log(`contador = ${contador}`)
    contador++
}
// exemplo 1 usando for
for (let i = 1; i <= 10; i++) {
    console.log(`contador = ${i}`)
}

// exemplo 2 usando array
const notas = [6.7, 7.4, 9.8, 8.1, 7.7]
for (i = 0; i < notas.length; i++) {
    console.log(`notas = ${notas[i]}`)
}